""" A very very simple set of calculators"""


def square(a):
    """a very simple squaring function

    long description: returns the square of a: :math:`a^2`

    :param a: an input argument

    :returns: a*a
    """
    return a * a


def multiply(a, b):
    """a very simple multiplying function

    long description: returns the square of b: :math:`a*b`

    :param a: an input argument
    :param b: an input argument

    :returns: a*b
    """
    return a * b
